TUNNEL_PID=$!
sleep 5
kill $TUNNEL_PID 2>/dev/null || true

echo -e "${GREEN}âœ… Tunnel configuration appears valid${NC}"

echo ""
echo "=============================================="
echo -e "${GREEN}ğŸ‰ TUNNEL B SETUP COMPLETE!${NC}"
echo "=============================================="
echo ""
echo "ğŸ“‹ NEXT STEPS:"
echo ""
echo "1. ${YELLOW}CONFIGURE SUPABASE IP RESTRICTIONS:${NC}"
echo "   â€¢ Go to Supabase Dashboard â†’ Settings â†’ Database"
echo "   â€¢ Click 'Network restrictions'"
echo "   â€¢ Enable restrictions"
echo "   â€¢ Add ONLY these Cloudflare IP ranges:"
echo ""
echo "   173.245.48.0/20"
echo "   103.21.244.0/22"
echo "   103.22.200.0/22"
echo "   103.31.4.0/22"
echo "   141.101.64.0/18"
