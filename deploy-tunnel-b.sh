echo ""

# Color codes
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

# Load environment variables
if [ -f .env ]; then
    export $(cat .env | grep -v '#' | awk '/=/ {print $1}')
fi

# Function to check command availability
check_command() {
    if ! command -v $1 &> /dev/null; then
        echo -e "${RED}âŒ $1 is not installed${NC}"
        return 1
    fi
    echo -e "${GREEN}âœ… $1 is installed${NC}"
    return 0
}

# Function to extract Supabase project reference
extract_project_ref() {
    echo "$1" | sed -n 's|https://\([^.]*\)\.supabase\.co|\1|p'
}

echo "ğŸ“‹ Step 1: Checking prerequisites..."
echo ""

# Check required commands
check_command "cloudflared" || exit 1
check_command "node" || exit 1
check_command "npm" || exit 1

echo ""
echo "ğŸ“‹ Step 2: Validating environment configuration..."
echo ""

# Check for Tunnel A token
if [ -z "$CLOUDFLARE_TUNNEL_TOKEN" ]; then
    echo -e "${YELLOW}âš ï¸  CLOUDFLARE_TUNNEL_TOKEN not found (Tunnel A)${NC}"
else
    echo -e "${GREEN}âœ… Tunnel A token configured${NC}"
fi

# Check Supabase URL
if [ -z "$SUPABASE_URL" ]; then
    echo -e "${RED}âŒ SUPABASE_URL not set in .env${NC}"
    echo "   Please add: SUPABASE_URL=https://your-project-ref.supabase.co"
    exit 1
fi
echo -e "${GREEN}âœ… Supabase URL: $SUPABASE_URL${NC}"

# Extract project reference
SUPABASE_PROJECT_REF=$(extract_project_ref "$SUPABASE_URL")
if [ -z "$SUPABASE_PROJECT_REF" ]; then
    echo -e "${RED}âŒ Could not extract Supabase project reference${NC}"
    exit 1
fi
echo -e "${GREEN}âœ… Project Reference: $SUPABASE_PROJECT_REF${NC}"

echo ""
echo "ğŸ“‹ Step 3: Creating Supabase tunnel..."
echo ""

# Check if tunnel B token exists
if [ -z "$CLOUDFLARE_SUPABASE_TUNNEL_TOKEN" ]; then
    echo -e "${YELLOW}Creating new tunnel 'supabase-secure-tunnel'...${NC}"
    
    # Try to create the tunnel
    if cloudflared tunnel create supabase-secure-tunnel 2>&1 | grep -q "already exists"; then
        echo -e "${GREEN}âœ… Tunnel already exists${NC}"
    else
        echo -e "${GREEN}âœ… Tunnel created successfully${NC}"
    fi
    
    echo ""
    echo -e "${YELLOW}ğŸ“Œ IMPORTANT: You need to get the tunnel token${NC}"
    echo "1. Go to: https://one.dash.cloudflare.com/"
    echo "2. Navigate to: Zero Trust â†’ Access â†’ Tunnels"
    echo "3. Click on: supabase-secure-tunnel"
    echo "4. Copy the tunnel token"
    echo "5. Add to .env: CLOUDFLARE_SUPABASE_TUNNEL_TOKEN=\"your_token\""
    echo ""
    read -p "Press Enter after adding the token to continue..."
    
    # Reload environment
    export $(cat .env | grep -v '#' | awk '/=/ {print $1}')
    
    if [ -z "$CLOUDFLARE_SUPABASE_TUNNEL_TOKEN" ]; then
        echo -e "${RED}âŒ Token still not found in .env${NC}"
        exit 1
    fi
else
    echo -e "${GREEN}âœ… Tunnel B token already configured${NC}"
fi

echo ""
echo "ğŸ“‹ Step 4: Creating tunnel configuration file..."
echo ""

# Create the supabase-tunnel.yml with proper variable substitution
cat > supabase-tunnel.yml << EOF
