loglevel: info
grace-period: 30s
retries: 5
EOF

echo -e "${GREEN}âœ… Created supabase-tunnel.yml${NC}"

echo ""
echo "ğŸ“‹ Step 5: Configuring DNS..."
echo ""

# Configure DNS route
echo "Adding DNS route for db-secure.888intelligenceautomation.in..."
if cloudflared tunnel route dns supabase-secure-tunnel db-secure.888intelligenceautomation.in 2>&1 | grep -q "already exists"; then
    echo -e "${GREEN}âœ… DNS route already exists${NC}"
else
    echo -e "${GREEN}âœ… DNS route created${NC}"
fi

echo ""
echo "ğŸ“‹ Step 6: Installing Supabase dependencies..."
echo ""

# Check if @supabase/supabase-js is installed in frontend
