    echo "Installing @supabase/supabase-js for frontend..."
    npm install @supabase/supabase-js
    echo -e "${GREEN}âœ… Frontend Supabase client installed${NC}"
else
    echo -e "${GREEN}âœ… Frontend Supabase client already installed${NC}"
fi

# Install for backend if backend directory exists
if [ -d "backend" ]; then
    cd backend
    if ! grep -q "@supabase/supabase-js" package.json 2>/dev/null; then
        echo "Installing @supabase/supabase-js for backend..."
        npm install @supabase/supabase-js
        echo -e "${GREEN}âœ… Backend Supabase client installed${NC}"
    else
        echo -e "${GREEN}âœ… Backend Supabase client already installed${NC}"
    fi
    cd ..
fi

echo ""
echo "ğŸ“‹ Step 7: Testing tunnel configuration..."
echo ""

# Quick test of tunnel configuration
